#include "lunaluacompiler.h"
#include "../../defines.h"

LunaLuaCompiler::LunaLuaCompiler(QList<EventCommand *> commands, QObject *parent) :
    BasicCompiler(commands, parent)
{}


LunaLuaCompiler::~LunaLuaCompiler()
{}

QString LunaLuaCompiler::compileCode()
{
    QString compiledCode;
    compiledCode += "--[[\n";
    compiledCode += "\t\tThis code was generated by PGE!\n\n";
    compiledCode += "\t\tYou're free to modify this code but recompiling will overwrite\n";
    compiledCode += "\t\tuser-modified code!\n\n";
    compiledCode += "]]\n\n\n\n";

    for(int i = 0; i < m_events.size(); ++i){
        compiledCode += m_events[i]->compileSegment(Script::COMPILER_LUNALUA, 0);
        compiledCode += "\n\n\n";
    }

    return compiledCode;
}


